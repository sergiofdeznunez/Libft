#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	char			*aux;

	i = 0;
	aux = (char *)s;
	while (i < n)
	{
		(*(aux + i)) = 0;
		i++;
	}
}
