#include <stdio.h>
#include "libft.h"

int		main()
{
	char			**cad = ft_split("", ' ');
	unsigned int	i;
	
	i = 0;
	while (*(cad + i))
	{
		printf("%s\n", *(cad + i));
		i++;
	}
	i = 0;
	while (*(cad + i))
	{
		free(*(cad + i));
		i++;
	}
	free(cad);
}
